def madlib
  puts "enter a noun"
  noun= gets.chomp
  puts "enter a verb"
  verb = gets.chomp
  puts "enter an adjective eg. a colour"
  adjective = gets.chomp
  puts "enter an adverb eg. fast"
  adverb = gets.chomp
